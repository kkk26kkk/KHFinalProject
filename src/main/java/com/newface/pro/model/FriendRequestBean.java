package com.newface.pro.model;

public class FriendRequestBean {
  private String request_id;
  private String accept_id;
  private int m_friend;

  public String getRequest_id() {
    return request_id;
  }

  public void setRequest_id(String request_id) {
    this.request_id = request_id;
  }

  public String getAccept_id() {
    return accept_id;
  }

  public void setAccept_id(String accept_id) {
    this.accept_id = accept_id;
  }

  public int getM_friend() {
    return m_friend;
  }

  public void setM_friend(int m_friend) {
    this.m_friend = m_friend;
  }
}
